package cm.lx.bean;

public class MortgageRebate {
    Integer id;

    String carModel;

    String number;

    Long loanDate;

    String billPerson;

    Double loan;

    String interestRate;

    Double backMoney;

    String abs;

    Long ctime;

    Long utime;

    String strLoanDate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCarModel() {
        return carModel;
    }

    public void setCarModel(String carModel) {
        this.carModel = carModel;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public Long getLoanDate() {
        return loanDate;
    }

    public void setLoanDate(Long loanDate) {
        this.loanDate = loanDate;
    }

    public String getBillPerson() {
        return billPerson;
    }

    public void setBillPerson(String billPerson) {
        this.billPerson = billPerson;
    }

    public Double getLoan() {
        return loan;
    }

    public void setLoan(Double loan) {
        this.loan = loan;
    }

    public String getInterestRate() {
        return interestRate;
    }

    public void setInterestRate(String interestRate) {
        this.interestRate = interestRate;
    }

    public Double getBackMoney() {
        return backMoney;
    }

    public void setBackMoney(Double backMoney) {
        this.backMoney = backMoney;
    }

    public String getAbs() {
        return abs;
    }

    public void setAbs(String abs) {
        this.abs = abs;
    }

    public Long getCtime() {
        return ctime;
    }

    public void setCtime(Long ctime) {
        this.ctime = ctime;
    }

    public Long getUtime() {
        return utime;
    }

    public void setUtime(Long utime) {
        this.utime = utime;
    }

    public String getStrLoanDate() {
        return strLoanDate;
    }

    public void setStrLoanDate(String strLoanDate) {
        this.strLoanDate = strLoanDate;
    }
}